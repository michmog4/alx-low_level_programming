#include <stdlib.h>
#include <time.h>
#include <stdio.h>
/**
 * main - Entry point
 *
 * Return: Always 0 (Success)
 */
int main(void)
{
	int a = '0';
	int b = '9';

	while (a <= b)
	{
		putchar(a);
	while (a != '9')
	{
	putchar(',');
	putchar(' ');
	goto s;
}
s:
a++;
}
putchar('\n');
return (0);
}
